                <h3 class="color-yellow">Estação Ferroviária União</h3>
                <p>O projeto da Estação Ferroviária União resgata a história da Guerra do Contestado. Conflito entre
                    governos, políticos, uma empresa multinacional e fazendeiros contra sertanejos, “caboclos posseiros”
                    que deveriam ser expulsos da terra valorizada com a chegada da ferrovia. Em 1916 os Estados assinam
                    um Acordo de Limites dividindo o quadro urbano da antiga União da Vitória pela linha dos trilhos da
                    Estrada de Ferro São Paulo – Rio Grande. A construção da estação em arco e no estilo Art Déco, foi
                    idealizada para “sarar as feridas da guerra”. Tem as bases da grande estrutura construída uma no
                    Paraná, outra em Santa Catarina. Busca a “união” das cidades divididas. O projeto foi concebido por
                    João W. Ficinski Dunin. É uma das estações mais belas e singulares do interior do Brasil. Possui um
                    túnel para pedestres ligando as duas cidades. A construção foi concluída durante o Governo Vargas,
                    em 1941.</p>
